// print total series number list base on position number

const fibonacciSeries = (position, series = [0, 1]) => {

    const totalNumbers = series.length;
    const _1st = series[totalNumbers - 1];
    const _2nd = series[totalNumbers - 2];
    const updatedSeries = [...series, _1st + _2nd];

    return position <= 1
        ? series
        : fibonacciSeries(position - 1, updatedSeries);
};

